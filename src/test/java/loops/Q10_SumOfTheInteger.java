package loops;

public class Q10_SumOfTheInteger {
    /*
    Type code to find the sum of the integers from 3 to 14
     */
    public static void main(String[] args) {

        int sum=0;
        for (int i = 3; i <15 ; i++) {
            sum+=i;

        }

        System.out.println(sum);


    }
}
